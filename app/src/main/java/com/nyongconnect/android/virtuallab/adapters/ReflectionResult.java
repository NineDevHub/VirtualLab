package com.nyongconnect.android.virtuallab.adapters;

public class ReflectionResult {

    int incidence;
    float reflection;
    public ReflectionResult(float i, float r){
        reflection = i;
        reflection = r;

    }

    public int getIncidence() {
        return incidence;
    }

    public float getReflection() {
        return reflection;
    }
}
